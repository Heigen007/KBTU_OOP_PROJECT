/**
 * The TeacherTitle enum represents the different titles that can be assigned to a teacher.
 *
 * @version 1.0
 * @author Zhanibek
 */
package universityProject.dev.users;

public enum TeacherTitle {
	/**
     * The title for a tutor.
     */
    TUTOR, 
    /**
     * The title for a lector.
     */
    LECTOR, 
    /**
     * The title for a senior lector.
     */
    SENIOR_LECTOR, 
    /**
     * The title for a professor.
     */
    PROFESSOR
}
