package universityProject.dev.research;

/**
 * The CiteFormat enum represents different formats for citing research papers.
 * It provides two formats: PLAIN_TEXT and BIBTEX.
 */
public enum CiteFormat {
    /**
     * Represents the plain text format for citing research papers.
     */
    PLAIN_TEXT,

    /**
     * Represents the BibTeX format for citing research papers.
     */
    BIBTEX
}
