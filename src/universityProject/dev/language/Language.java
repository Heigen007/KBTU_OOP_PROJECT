package universityProject.dev.language;

/**
 * The Language enum represents the available language options for the application.
 * Each enum constant corresponds to a specific language.
 */
public enum Language {
    /**
     * Represents the Russian language.
     */
    RU,

    /**
     * Represents the Kazakh language.
     */
    KZ,

    /**
     * Represents the English language.
     */
    EN
}
