package universityProject.dev.academicEntities;

/**
 * @author Asyl
 * @version 18.12.2023
 * The NewsTopic enum represents the topic of a news article, which can be RESEARCH, ORDINARY, or JOURNAL.
 */
public enum NewsTopic {

    /** Represents a research-oriented news article. */
    RESEARCH,

    /** Represents an ordinary news article. */
    ORDINARY,

    /** Represents a journal-related news article. */
    JOURNAL
}
